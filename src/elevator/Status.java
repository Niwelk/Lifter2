package elevator;

/**
 * Перечисление {@code Status} представляет возможные состояния лифта.
 * Каждое состояние определяет текущий режим работы лифта и используется
 * для управления его поведением и отображения статуса в системе.
 *
 * <p>Состояния лифта:
 * <ul>
 *   <li>{@link #MOVING} - лифт движется между этажами</li>
 *   <li>{@link #STOPPED} - лифт остановился на этаже, двери закрыты</li>
 *   <li>{@link #DOORS_OPENING} - лифт открывает двери после остановки</li>
 *   <li>{@link #DOORS_CLOSING} - лифт закрывает двери перед началом движения</li>
 * </ul>
 *
 * @see Elevator
 * @see Dispatcher
 */
 enum Status {
    /**
     * Находится в движении
     */
    MOVING,

    /**
     * Остановился
     */
    STOPPED,

    /**
     * Двери открываются
     */
    DOORS_OPENING,

    /**
     * Двери закрываются
     */
    DOORS_CLOSING
}